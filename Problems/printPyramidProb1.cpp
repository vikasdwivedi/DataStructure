#include <iostream>

using namespace std;

int main()
{
    int n = 0, j=0;
    cin>>n;
   
   for( int i = 1; i <= n; i++ ) {
        
        j = 1;
        while(j<=i) {
            cout<<j;
            j++;
        }
        
        j = (n-i)*2;
        while( j > 0 ) {
            cout<< " ";
            j--;
        }
        
        j = i;
        while( j > 0) {
            cout<<j--;
        }
        
        cout<<endl;
   }
}
